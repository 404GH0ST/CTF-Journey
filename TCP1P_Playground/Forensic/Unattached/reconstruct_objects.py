    decompressed_data = zlib.decompress(data)
    sha1_hash = hashlib.sha1(decompressed_data).hexdigest()
    directory = sha1_hash[:2]
    filename = sha1_hash[2:]
    os.makedirs(".git/objects/" + directory, exist_ok=True)
    with open(os.path.join(".git/objects/" + directory, filename), "wb") as f:
        f.write(data)
